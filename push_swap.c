#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*a;
	t_stack	*b;

	a = NULL;
	a = ft_check(argc, argv);
	// if (!a || ft_issorted(&a))
	// 	return (0);
	ft_issorted(&a);
	b = NULL;
	ft_sort(&a, &b);
	return (0);
}
